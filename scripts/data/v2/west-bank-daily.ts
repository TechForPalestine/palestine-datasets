import { ApiResource } from "../../../types/api.types";
import { writeJson } from "../../utils/fs";
import { validateDailiesJson } from "../common/casualties-daily";
import {
  gazaContentDir,
  westBankCarryFields,
  westBankContentDir,
  westBankIncrementalRules,
} from "../common/casualties-daily/config";
import {
  applyIncrementalToCumulative,
  buildCarryForwardTimeline,
  latestReportDate,
  readDailyReports,
} from "../common/casualties-daily/content";

const jsonFileName = "west_bank_daily.json";

const generateJsonFromContent = () => {
  // West Bank source files exist only for actual report dates; fill days are
  // generated by carrying forward to the latest Gaza report date so the two
  // daily series stay in sync.
  const reports = readDailyReports(westBankContentDir);
  applyIncrementalToCumulative(reports, westBankIncrementalRules);
  const endDate = latestReportDate(gazaContentDir);
  const series = buildCarryForwardTimeline(reports, {
    carryFields: westBankCarryFields,
    sparseField: "verified",
    sourceField: "flash_source",
    endDate,
  });
  validateDailiesJson(series);
  writeJson(ApiResource.WestBankDailyV2, jsonFileName, series);
  console.log(
    `generated JSON file: ${jsonFileName} from ${reports.length} reports` +
      ` expanded to ${series.length} daily rows (through ${endDate})`,
  );
};

generateJsonFromContent();
